create database Timetable

create table Departments(
D_ID int primary key,
D_Name varchar(40) not null unique,
Teacher_num int 
)

create table Teachers(
Teacher_ID int primary key,
T_Fname varchar(20) not null,
T_Lname varchar(50) not null,
Email varchar(30) default 'Create new email',
Phone varchar(13) not null unique,
D_ID int foreign key references Departments(D_ID),
Tutor bit not null  
)
alter table Teachers add constraint chck check(charindex('@', Email)>0 and charindex(' ',Email)=0)
alter table Teachers add constraint chck2 check(Phone like '+48[4-8]%')

create table Subjects(
S_ID int primary key,
S_Name varchar(30) not null unique,
Teacher_ID int foreign key references Teachers(Teacher_ID)
)
alter table Subjects alter column S_Name varchar(50)
 
create table Tutors(
Tutor_ID int primary key,
Teacher_ID int foreign key references Teachers(Teacher_ID)
)

create table Classes(
Class_ID int primary key,
Class_Name varchar(10) not null unique,
Class_Level tinyint,
Tutor_ID int foreign key references Tutors(Tutor_ID)
)
alter table Classes alter column Class_Name varchar(50)

create table Rooms(
Room_ID int primary key,
Room_Name varchar(15) not null default 'On Teams',
Capacity int not null default 1000
)

alter table rooms alter column Room_Name varchar(40)

create table Tdays(
Day_ID int primary key,
Day_Name varchar(9) not null
)
alter table Tdays add constraint chck3 
check(Day_name in('Monday','Tuesday','Wednesday','Thursday','Friday','Saturday','Sunday'))

create table Tperiods(
Period_ID int primary key,
Period_Name varchar(15) not null,
S_Date date,
E_date date
)

create table TimeTable(
TimeTable_ID int primary key,
Teacher_ID int foreign key references Teachers(Teacher_ID),
S_ID int foreign key references Subjects(S_ID),
Class_ID int foreign key references Classes(Class_ID),
Day_ID int foreign key references Tdays(Day_ID),
Room_ID int foreign key references Rooms(Room_ID),
Period_ID int foreign key references Tperiods(Period_ID)
)

